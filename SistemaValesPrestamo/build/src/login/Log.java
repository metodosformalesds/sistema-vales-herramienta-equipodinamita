    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuAd = "Admin";
        String contrAd = "123";
        String usuBec = "Becario";
        String contrBec = "321";
        
        if(usuario.getText().length()==0 && contra.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Ingrese Datos en los campos vacios"); 
        }else{
            if(usuario.getText().length()==0){
                JOptionPane.showMessageDialog(null, "Ingrese su usuario");
            }else{
                if(contra.getText().length()==0){
                JOptionPane.showMessageDialog(null, "Ingrese contraseña");
            }else{
                if(usuario.getText().equals(usuAd)&&contra.getText().equals(contrAd)){
                this.dispose();
                new menuPrincipal().setVisible(true);
            }else{
                    if(usuario.getText().equals(usuBec)&&contra.getText().equals(contrBec)){
                this.dispose();
                new MenuPrincipalBecario().setVisible(true);
                    }else{
                                     JOptionPane.showMessageDialog(null, "Usuario o contraseña erroneas");   
                    }
            }  
                }
        }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

