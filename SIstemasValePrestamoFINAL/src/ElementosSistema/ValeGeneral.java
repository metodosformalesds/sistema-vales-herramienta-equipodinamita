package ElementosSistema;
public class ValeGeneral {
    int folio; 
    String horaEntrada; 
    String horaSalida; 
    String fecha;
    String docente; 
    int matEntrega; 
    int matRecibe;
    String nombreSolicitante;
    int matriculaSolicitante;
    String status;

    public int getFolio() {
        return folio;
    }

    public void setFolio(int folio) {
        this.folio = folio;
    }

    public String getHoraEntrada() {
        return horaEntrada;
    }

    public void setHoraEntrada(String horaEntrada) {
        this.horaEntrada = horaEntrada;
    }

    public String getHoraSalida() {
        return horaSalida;
    }

    public void setHoraSalida(String horaSalida) {
        this.horaSalida = horaSalida;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getDocente() {
        return docente;
    }

    public void setDocente(String docente) {
        this.docente = docente;
    }

    public int getMatEntrega() {
        return matEntrega;
    }

    public void setMatEntrega(int matEntrega) {
        this.matEntrega = matEntrega;
    }

    public int getMatRecibe() {
        return matRecibe;
    }

    public void setMatRecibe(int matRecibe) {
        this.matRecibe = matRecibe;
    }

    public String getNombreSolicitante() {
        return nombreSolicitante;
    }

    public void setNombreSolicitante(String nombreSolicitante) {
        this.nombreSolicitante = nombreSolicitante;
    }

    public int getMatriculaSolicitante() {
        return matriculaSolicitante;
    }

    public void setMatriculaSolicitante(int matriculaSolicitante) {
        this.matriculaSolicitante = matriculaSolicitante;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
